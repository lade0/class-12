# making a star
import turtle
turtle.Screen().bgcolor("turquoise")
turtle.Screen().setup(300,400)
star=turtle.Turtle()

star.forward(105)
star.left(120)
star.forward(105)
star.left(120)
star.forward(105)
star.penup()
star.right(150)
star.forward(50)
star.pendown()
star.right(90)
star.forward(105)
star.right(120)
star.forward(105)
star.right(120)
star.forward(105)
turtle.done()